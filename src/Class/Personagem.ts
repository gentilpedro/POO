export class Personagem {
    nome: string = " "; //Nome 
    nivel: number = 0; //Nivel do personagem
    classe:string = " "; //Classe ex: guerreiro , mago, bardo
    vida: number = 0; // vida do personagem
    ataque:number = 0; //Ataque do personagem
    defesa:number = 0; //Defesa e o numero de defesa que o personagem tem
    raca:string = " "; //define a raça do personagem ex: elfo, anão etc
    stamina:number = 0; //Stamina é a energia que o personagem tem para realizar ações
    mana:number = 0; //Mana é a energia que o personagem tem para realizar magias
    poderAtaque:number = 0; //Poder de ataque é a quantidade de dano que o personagem pode causar
    intelecto:number = 0; //Intelecto é a inteligência do personagem
    armadura:number = 0; //Armadura é a resistência do personagem a ataques
    energia: number = 100;
    xp:number = 0;  //Energia é a quantidade de energia que o personagem tem para treinar 
};
